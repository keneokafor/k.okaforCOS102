tuple = ("Ekiti", 750, 'Oshogbo', 250, "Akure", 500)
s_tuple = ("Abeokuta", 300, "Ogbomoso")

# prints the complete tuple
print (tuple)

# prints the last element of the table
print (tuple[-1])

# prints the elements of the tuple starting from 2nd till 3rd
print (tuple[2:4])

# prints the elements of the tuple starting from the 3rd element
print (tuple[3:])

# prints the contents of the tuple twice
print (s_tuple * 3)

# prints catenated tuples
print (tuple + s_tuple)

